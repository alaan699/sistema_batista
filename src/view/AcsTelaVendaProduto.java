    private void acs_jBtnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acs_jBtnokActionPerformed

        AcsVendasProduto acsVendasProduto = new AcsVendasProduto();
        acsVendasProduto.setAcsProduto((AcsProduto) acs_jCboproduto.getSelectedItem() );
        acsVendasProduto.setAcsQuantidade(acs_jTxtquantidade.getText() );
        acsVendasProduto.setAcsValorUnitario(Util.strDouble(acs_jTxtvalor.getText()));
        if (getTitle().toUpperCase().substring(0, 1).equals("I")) {
           acsTelaVendas.vendasProdutoControle.addBean(acsVendasProduto);
        } else {            
            acsTelaVendas.vendasProdutoControle.updateBean(acsTelaVendas.getSelectedRowProd(), acsVendasProduto);
        }
        setVisible(false);
        
    }//GEN-LAST:event_acs_jBtnokActionPerformed

    private void acs_jCboprodutoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_acs_jCboprodutoItemStateChanged
        acs_jTxtquantidade.setText("1");
        AcsProduto acsProduto = (AcsProduto) acs_jCboproduto.getSelectedItem();
        acs_jTxtvalor.setText(Util.doubleStr(acsProduto.getAcsPreco()));
        acs_jTxttotal.setText(acs_jTxtvalor.getText());
    }//GEN-LAST:event_acs_jCboprodutoItemStateChanged

    private void acs_jTxtquantidadeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_acs_jTxtquantidadeKeyReleased
        if(acs_jTxtquantidade.getText().isEmpty() == false){
            double unitario = Util.strDouble(acs_jTxtvalor.getText());
            double quantidade = Util.strInt(acs_jTxtquantidade.getText());
            acs_jTxttotal.setText(Util.doubleStr(quantidade * unitario));
        } else{
        acs_jTxttotal.setText("0");
        }
        
    }//GEN-LAST:event_acs_jTxtquantidadeKeyReleased

    private void acs_jTxtvalorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_acs_jTxtvalorKeyReleased
        if(acs_jTxtvalor.getText().isEmpty() == false){
            double unitario = Util.strDouble(acs_jTxtvalor.getText());
            double quantidade = Util.strInt(acs_jTxtquantidade.getText());
            acs_jTxttotal.setText(Util.doubleStr(quantidade * unitario));
        } else{
        acs_jTxttotal.setText("0");
        }
    }//GEN-LAST:event_acs_jTxtvalorKeyReleased

